#pragma once

#include "global.h"

#include <cstdint>

class CommandParser
{
public:
    class Owner
    {
        friend class CommandParser;

        virtual void onCommandSetupSignal(Waveform waveform,
                                          unsigned int frequency,
                                          double amplitude) = 0;
        virtual void onCommandSetGain(Gain gain) = 0;
        virtual void onCommandReset() = 0;
    };

public:
    CommandParser(Owner* owner);

    void parse(uint32_t command);

private:
    Owner* m_owner;
};
